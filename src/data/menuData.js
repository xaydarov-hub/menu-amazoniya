/** Kategoriya ID si bo'yicha asosiy menu kategoriyasini topadi */
export const getAsosiyKategoriya = (id) =>
  asosiyMenu.find((k) => k.id === id);

/** Kategoriya ID si bo'yicha bar kategoriyasini topadi */
export const getBarKategoriya = (id) =>
  barMenu.find((k) => k.id === id);

/** Mahsulot ID si bo'yicha istalgan menuda qidiradi */
export const getMahsulot = (mahsulotId) => {
  for (const kat of [...asosiyMenu, ...barMenu]) {
    const m = kat.mahsulotlar.find((x) => x.id === mahsulotId);
    if (m) return { ...m, kategoriya: kat };
  }
  return null;
};

/** Ommabop mahsulotlarni qaytaradi (har ikki mendan) */
export const getOmmaboplar = () => {
  const hammasi = [...asosiyMenu, ...barMenu].flatMap((kat) =>
    kat.mahsulotlar
      .filter((m) => m.ommabop)
      .map((m) => ({ ...m, kategoriyaNomi: kat.nomi, kategoriyaId: kat.id }))
  );
  return hammasi;
};

/** Tegga qarab filtrlaydi */
export const filterByTeg = (teg) =>
  [...asosiyMenu, ...barMenu].flatMap((kat) =>
    kat.mahsulotlar
      .filter((m) => m.teglar?.includes(teg))
      .map((m) => ({ ...m, kategoriyaNomi: kat.nomi }))
  );

